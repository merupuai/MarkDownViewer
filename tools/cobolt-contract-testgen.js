#!/usr/bin/env node

// CoBolt Contract Test Generator (v0.12.0 — WS1)
//
// Reads interface-contracts.json and emits executable contract test files
// under tests/contracts/ — one file per contract ID. The generated tests
// drive the L3 replay verification performed by cobolt-contract-semantic-verify.js.
//
// Language detection (auto):
//   - package.json + jest/vitest config → JavaScript
//   - mix.exs                           → Elixir (ExUnit)
//   - pyproject.toml / setup.py         → Python (pytest)
//   - go.mod                            → Go (go test)
//   - fallback                          → JavaScript (node --test)
//
// The generated tests are SKELETONS — they assert example.then against
// replayed response using project-native HTTP/DB/event client. Builders
// are expected to flesh in the actual transport call, but the assertions
// (shape + invariants) are emitted verbatim from the contract.
//
// A contract-replay.json report is expected at _cobolt-output/latest/test/
// after tests run — cobolt-contract-semantic-verify.js L3 reads it.
//
// Usage:
//   node tools/cobolt-contract-testgen.js emit           # generate all
//   node tools/cobolt-contract-testgen.js emit --id IC-API-001
//   node tools/cobolt-contract-testgen.js stack          # print detected stack

const fs = require('node:fs');
const path = require('node:path');

function paths() {
  try {
    const mod = require('../lib/cobolt-paths');
    const p = typeof mod === 'function' ? mod() : typeof mod.paths === 'function' ? mod.paths() : mod;
    return p;
  } catch {
    const out = path.join(process.cwd(), '_cobolt-output');
    return {
      outputRoot: out,
      latestPlanning: () => path.join(out, 'latest', 'planning'),
    };
  }
}

function loadContracts() {
  const p = paths();
  const candidates = [
    path.join(typeof p.latestPlanning === 'function' ? p.latestPlanning() : '', 'interface-contracts.json'),
    path.join(process.cwd(), '_cobolt-output', 'latest', 'planning', 'interface-contracts.json'),
    path.join(process.cwd(), '_cobolt-output', 'planning', 'interface-contracts.json'),
  ];
  for (const c of candidates) {
    if (c && fs.existsSync(c)) {
      try {
        return { data: JSON.parse(fs.readFileSync(c, 'utf8')), source: c };
      } catch (err) {
        throw new Error(`invalid JSON in ${c}: ${err.message}`);
      }
    }
  }
  return { data: null, source: null };
}

function detectStack() {
  const cwd = process.cwd();
  if (fs.existsSync(path.join(cwd, 'mix.exs'))) return 'elixir';
  if (fs.existsSync(path.join(cwd, 'go.mod'))) return 'go';
  if (fs.existsSync(path.join(cwd, 'pyproject.toml')) || fs.existsSync(path.join(cwd, 'setup.py'))) return 'python';
  if (fs.existsSync(path.join(cwd, 'package.json'))) return 'javascript';
  return 'javascript';
}

// ── JavaScript emitter (node --test / jest / vitest compatible) ─────

function emitJavaScript(contract) {
  const examples = contract.examples || [];
  const invariants = contract.invariants || [];
  const lines = [];
  lines.push('// AUTO-GENERATED by cobolt-contract-testgen.js — do not edit manually.');
  lines.push(`// Contract: ${contract.id} (${contract.type}) — provider ${contract.provider}`);
  lines.push(`// Regenerate: node tools/cobolt-contract-testgen.js emit --id ${contract.id}`);
  lines.push('');
  lines.push("const test = require('node:test');");
  lines.push("const assert = require('node:assert/strict');");
  lines.push("const fs = require('node:fs');");
  lines.push("const path = require('node:path');");
  lines.push('');
  lines.push(`// Replay harness — records per-example pass/fail to contract-replay.json`);
  lines.push('function record(result) {');
  lines.push("  const out = path.join(process.cwd(), '_cobolt-output', 'latest', 'test');");
  lines.push('  fs.mkdirSync(out, { recursive: true });');
  lines.push("  const file = path.join(out, 'contract-replay.json');");
  lines.push('  let report = { generatedAt: new Date().toISOString(), results: [] };');
  lines.push("  if (fs.existsSync(file)) { try { report = JSON.parse(fs.readFileSync(file, 'utf8')); } catch {} }");
  lines.push(`  let entry = report.results.find(r => r.contractId === ${JSON.stringify(contract.id)});`);
  lines.push(
    `  if (!entry) { entry = { contractId: ${JSON.stringify(contract.id)}, total: 0, passed: 0, failed: 0, examples: [] }; report.results.push(entry); }`,
  );
  lines.push('  // v0.12.0 fix H2: upsert-by-exampleId so re-runs replace prior results');
  lines.push('  const existingIdx = entry.examples.findIndex(e => e && e.exampleId === result.exampleId);');
  lines.push('  if (existingIdx >= 0) entry.examples[existingIdx] = result;');
  lines.push('  else entry.examples.push(result);');
  lines.push('  entry.total = entry.examples.length;');
  lines.push('  entry.passed = entry.examples.filter(e => e.ok).length;');
  lines.push('  entry.failed = entry.examples.filter(e => !e.ok).length;');
  lines.push('  entry.lastRun = new Date().toISOString();');
  lines.push('  fs.writeFileSync(file, JSON.stringify(report, null, 2));');
  lines.push('}');
  lines.push('');

  for (const ex of examples) {
    const name = `${contract.id} ${ex.id} [${ex.kind || 'happy'}] ${ex.description || ''}`.trim();
    lines.push(`test(${JSON.stringify(name)}, async () => {`);
    lines.push(`  const given = ${JSON.stringify(ex.given, null, 2)};`);
    lines.push(`  const when  = ${JSON.stringify(ex.when, null, 2)};`);
    lines.push(`  const then_ = ${JSON.stringify(ex.then, null, 2)};`);
    lines.push('  let ok = false, err = null;');
    lines.push('  try {');
    lines.push(`    // TODO (builder): wire project-native transport for ${contract.type} contract.`);
    lines.push('    //   API  : call app via supertest/fetch — assert response matches then_');
    lines.push('    //   DATA : run migration then query — assert row shape matches then_');
    lines.push('    //   EVT  : publish event then consume — assert payload matches then_');
    lines.push('    //   TYPE : import symbol — assert type shape matches then_');
    lines.push(`    throw new Error('contract replay not wired — builder must implement for ${contract.id}');`);
    lines.push('    // ok = true;  // enable once wired');
    lines.push('  } catch (e) { err = e; }');
    lines.push(
      `  record({ exampleId: ${JSON.stringify(ex.id)}, kind: ${JSON.stringify(ex.kind || 'happy')}, invariantRefs: ${JSON.stringify(ex.invariantRefs || [])}, ok, error: err ? String(err.message) : null });`,
    );
    lines.push('  if (err) throw err;');
    lines.push('  assert.ok(ok);');
    lines.push('});');
    lines.push('');
  }

  if (invariants.length) {
    lines.push('// Invariants pinned by this contract:');
    for (const inv of invariants)
      lines.push(`//   ${inv.id} [${inv.category || 'other'}/${inv.severity || 'high'}]: ${inv.statement}`);
  }

  return lines.join('\n');
}

// ── Elixir emitter (ExUnit skeleton) ────────────────────────────────

function emitElixir(contract) {
  const examples = contract.examples || [];
  const modName = contract.id.replace(/[^A-Za-z0-9]/g, '');
  const lines = [];
  lines.push('# AUTO-GENERATED by cobolt-contract-testgen.js — do not edit manually.');
  lines.push(`# Contract: ${contract.id} (${contract.type}) — provider ${contract.provider}`);
  lines.push('');
  lines.push(`defmodule Contract${modName}Test do`);
  lines.push('  use ExUnit.Case, async: false');
  lines.push('');
  lines.push('  @moduledoc """');
  lines.push(
    `  Replay of contract ${contract.id}. Records evidence to _cobolt-output/latest/test/contract-replay.json.`,
  );
  lines.push('  """');
  lines.push('');
  for (const ex of examples) {
    const name = `${ex.id} [${ex.kind || 'happy'}] ${ex.description || ''}`.trim();
    lines.push(`  test ${JSON.stringify(name)} do`);
    lines.push('    # TODO (builder): wire project-native transport — assert then shape.');
    lines.push(`    flunk("contract replay not wired for ${ex.id}")`);
    lines.push('  end');
    lines.push('');
  }
  lines.push('end');
  return lines.join('\n');
}

// ── Python emitter (pytest skeleton) ────────────────────────────────

function emitPython(contract) {
  const examples = contract.examples || [];
  const lines = [];
  lines.push('# AUTO-GENERATED by cobolt-contract-testgen.js — do not edit manually.');
  lines.push(`# Contract: ${contract.id} (${contract.type}) — provider ${contract.provider}`);
  lines.push('import json, pathlib, pytest');
  lines.push('');
  for (const ex of examples) {
    lines.push(`def test_${ex.id.replace(/[^A-Za-z0-9]/g, '_').toLowerCase()}():`);
    lines.push(`    """${contract.id} ${ex.id} [${ex.kind || 'happy'}] ${ex.description || ''}"""`);
    lines.push(`    given = ${JSON.stringify(ex.given)}`);
    lines.push(`    when_ = ${JSON.stringify(ex.when)}`);
    lines.push(`    then_ = ${JSON.stringify(ex.then)}`);
    lines.push(`    pytest.fail("contract replay not wired for ${ex.id}")`);
    lines.push('');
  }
  return lines.join('\n');
}

// ── Main ─────────────────────────────────────────────────────────────

function emitAll(opts = {}) {
  const { data } = loadContracts();
  if (!data) return { ok: true, skipped: true, reason: 'no interface-contracts.json' };
  const contracts = (data.contracts || []).filter((c) => !opts.id || c.id === opts.id);
  const stack = opts.stack || detectStack();
  const outDir = path.join(process.cwd(), 'tests', 'contracts');
  fs.mkdirSync(outDir, { recursive: true });

  const written = [];
  for (const c of contracts) {
    if (!(c.examples || []).length) continue;
    let body, ext;
    if (stack === 'elixir') {
      body = emitElixir(c);
      ext = '.exs';
    } else if (stack === 'python') {
      body = emitPython(c);
      ext = '.py';
    } else {
      body = emitJavaScript(c);
      ext = '.test.js';
    }
    const file = path.join(outDir, `${c.id}${ext}`);
    fs.writeFileSync(file, body);
    written.push(file);
  }
  return { ok: true, stack, written, count: written.length, totalContracts: contracts.length };
}

function parseFlags(args) {
  const out = { _: [], id: null, stack: null };
  for (let i = 0; i < args.length; i++) {
    if (args[i] === '--id') out.id = args[++i];
    else if (args[i] === '--stack') out.stack = args[++i];
    else out._.push(args[i]);
  }
  return out;
}

function main() {
  const [cmd, ...rest] = process.argv.slice(2);
  const flags = parseFlags(rest);
  switch (cmd) {
    case 'emit': {
      const r = emitAll(flags);
      console.log(JSON.stringify(r, null, 2));
      return 0;
    }
    case 'stack': {
      console.log(JSON.stringify({ stack: detectStack(), cwd: process.cwd() }, null, 2));
      return 0;
    }
    default:
      console.error(
        'Usage: cobolt-contract-testgen.js {emit|stack} [--id IC-API-001] [--stack javascript|elixir|python]',
      );
      return 1;
  }
}

if (require.main === module) process.exit(main());

module.exports = { emitAll, detectStack, loadContracts };
