#!/usr/bin/env node

// CoBolt README Gen - Deterministic README.md generator from planning artifacts
//
// Reads PRD, epics, architecture, TRD, milestones, and design-tokens.json
// to assemble a comprehensive project README.md.
//
// Usage:
//   node tools/cobolt-readme-gen.js generate                  # Write README.md to project root + planning dir
//   node tools/cobolt-readme-gen.js generate --output <path>  # Write to custom path only
//   node tools/cobolt-readme-gen.js check                     # Verify README exists and is fresh (exit 1 if stale)
//
// Exit codes:
//   0 = success
//   1 = stale or missing README
//   2 = usage error or missing planning artifacts

const fs = require('node:fs');
const path = require('node:path');
const { getPlanningDir, getLatestRoot, safeReadJson } = require('../lib/cobolt-planning-artifacts');

const WATERMARK = '<!-- Generated by CoBolt — re-run "node tools/cobolt-readme-gen.js generate" to update -->';
const PROJECT_ROOT = process.cwd();
const TOKENS_PATH = path.join(PROJECT_ROOT, 'design-tokens.json');

// ── Section extractors ─────────────────────────────────────

function loadTokensBrand() {
  try {
    const tokens = JSON.parse(fs.readFileSync(TOKENS_PATH, 'utf8'));
    return tokens.brand || {};
  } catch {
    return {};
  }
}

function extractPrdOverview(pd) {
  const prdPath = path.join(pd, 'prd.md');
  if (!fs.existsSync(prdPath)) return null;

  const content = fs.readFileSync(prdPath, 'utf8');
  const lines = content.split('\n');

  // Try to find executive summary or first major section after the title
  let collecting = false;
  const overview = [];
  let foundH2 = false;

  for (const line of lines) {
    // Skip the title line
    if (line.match(/^#\s+/) && !foundH2) continue;

    // Start collecting after first H2 that looks like overview/summary/vision
    if (line.match(/^##\s+(Executive\s+Summary|Overview|Vision|Product\s+Overview|Introduction|Summary)/i)) {
      collecting = true;
      foundH2 = true;
      continue;
    }

    // Stop at the next H2
    if (collecting && line.match(/^##\s+/)) break;

    // If we haven't found an explicit overview section, collect the first paragraph
    if (!foundH2 && !collecting && line.trim().length > 20 && !line.startsWith('#')) {
      collecting = true;
    }

    if (collecting) {
      if (line.match(/^##\s+/)) break;
      overview.push(line);
    }
  }

  const text = overview.join('\n').trim();
  // Limit to ~500 chars for README brevity
  return text.length > 500 ? `${text.slice(0, 497)}...` : text;
}

function extractProjectName(pd) {
  const prdPath = path.join(pd, 'prd.md');
  if (!fs.existsSync(prdPath)) return null;

  const content = fs.readFileSync(prdPath, 'utf8');
  const titleMatch = content.match(/^#\s+(.+)/m);
  if (titleMatch) {
    // Strip common PRD suffixes
    return titleMatch[1]
      .replace(/\s*[-–—]\s*Product\s+Requirements\s+Document.*/i, '')
      .replace(/\s*PRD.*/i, '')
      .replace(/\s*[-–—]\s*Requirements.*/i, '')
      .trim();
  }
  return null;
}

function extractEpicFeatures(pd) {
  const epicsPath = path.join(pd, 'epics.md');
  if (!fs.existsSync(epicsPath)) return [];

  const content = fs.readFileSync(epicsPath, 'utf8');
  const features = [];

  for (const line of content.split('\n')) {
    const epicMatch = line.match(/^##\s+(?:Epic\s+)?E[A-Z0-9_]+\s*[:\u2014-]\s*(.+)/i);
    if (epicMatch) {
      const name = epicMatch[1].replace(/\s*\(M\d+\)\s*/, '').trim();
      // Count stories under this epic
      const storyCount = (content.slice(content.indexOf(line)).match(/E[A-Z0-9_]+-S\d+/g) || []).length;
      features.push({ name, stories: Math.min(storyCount, 20) }); // cap at 20 for sanity
    }
  }

  return features;
}

function extractTechStack(pd) {
  const archPaths = [path.join(pd, 'system-architecture.md'), path.join(pd, 'architecture.md')];

  let content = '';
  for (const p of archPaths) {
    if (fs.existsSync(p)) {
      content = fs.readFileSync(p, 'utf8');
      break;
    }
  }

  if (!content) return [];

  const stack = [];
  const lines = content.split('\n');

  // Look for tech stack table or technology section
  let inTable = false;
  let headerParsed = false;

  for (const line of lines) {
    if (line.match(/^##\s+(Tech(nology)?\s+Stack|Technologies|Stack\s+Overview)/i)) {
      inTable = true;
      continue;
    }
    if (inTable && line.match(/^##\s+/)) break;

    if (inTable) {
      // Table row detection
      if (line.includes('|') && !line.match(/^[\s|:-]+$/)) {
        if (!headerParsed) {
          headerParsed = true;
          continue;
        }
        const cells = line
          .split('|')
          .map((c) => c.trim())
          .filter(Boolean);
        if (cells.length >= 2) {
          stack.push({ layer: cells[0], technology: cells[1], purpose: cells[2] || '' });
        }
      }

      // Bullet list detection
      const bulletMatch = line.match(/^[-*]\s+\*\*(.+?)\*\*\s*[:\u2014-]\s*(.+)/);
      if (bulletMatch) {
        stack.push({ layer: '', technology: bulletMatch[1], purpose: bulletMatch[2].trim() });
      }
    }
  }

  return stack;
}

function extractPrerequisites(pd) {
  const trdPath = path.join(pd, 'trd.md');
  if (!fs.existsSync(trdPath)) return [];

  const content = fs.readFileSync(trdPath, 'utf8');
  const prereqs = [];
  let inSection = false;

  for (const line of content.split('\n')) {
    if (line.match(/^##\s+(Prerequisites|Runtime|Development\s+Environment|Infrastructure)/i)) {
      inSection = true;
      continue;
    }
    if (inSection && line.match(/^##\s+/)) break;
    if (inSection) {
      const bulletMatch = line.match(/^[-*]\s+(.+)/);
      if (bulletMatch) prereqs.push(bulletMatch[1].trim());
    }
  }

  return prereqs;
}

function extractMilestones(pd) {
  const milestonesPath = path.join(pd, 'milestones.md');
  if (!fs.existsSync(milestonesPath)) return [];

  const content = fs.readFileSync(milestonesPath, 'utf8');
  const milestones = [];

  for (const line of content.split('\n')) {
    const msMatch = line.match(/^##\s+(?:Milestone\s+)?(M\d+)\s*[:\u2014-]\s*(.+)/i);
    if (msMatch) {
      milestones.push({
        id: msMatch[1].toUpperCase(),
        name: msMatch[2].trim(),
      });
    }
  }

  return milestones;
}

function extractSetupInstructions(latestRoot) {
  const manifestPath = path.join(latestRoot, 'infra', 'infra-manifest.json');
  if (!fs.existsSync(manifestPath)) return null;

  const manifest = safeReadJson(manifestPath);
  if (!manifest) return null;

  const instructions = [];

  if (manifest.platform?.composeFile || manifest.services) {
    instructions.push('```bash');
    instructions.push('# Start development infrastructure');
    instructions.push('docker compose up -d');
    instructions.push('');
    instructions.push('# Install dependencies');
    if (manifest.platform?.runtime === 'elixir') {
      instructions.push('mix deps.get');
      instructions.push('mix ecto.setup');
    } else if (manifest.platform?.runtime === 'node' || manifest.platform?.runtime === 'nodejs') {
      instructions.push('npm install');
    } else {
      instructions.push('# See architecture docs for dependency installation');
    }
    instructions.push('```');
  }

  return instructions.length > 0 ? instructions.join('\n') : null;
}

// ── README assembly ────────────────────────────────────────

function generateReadme(pd, latestRoot) {
  const brand = loadTokensBrand();
  const projectName = brand.name || extractProjectName(pd) || path.basename(PROJECT_ROOT);
  const tagline = brand.tagline || '';

  const overview = extractPrdOverview(pd);
  const features = extractEpicFeatures(pd);
  const techStack = extractTechStack(pd);
  const prereqs = extractPrerequisites(pd);
  const milestones = extractMilestones(pd);
  const setupInstructions = extractSetupInstructions(latestRoot);

  const lines = [];
  const ln = (s = '') => lines.push(s);

  ln(WATERMARK);
  ln();
  ln(`# ${projectName}`);
  if (tagline) ln(`> ${tagline}`);
  ln();

  // Overview
  if (overview) {
    ln('## Overview');
    ln();
    ln(overview);
    ln();
  }

  // Features
  if (features.length > 0) {
    ln('## Features');
    ln();
    for (const f of features) {
      const storyNote = f.stories > 0 ? ` (${f.stories} stories)` : '';
      ln(`- **${f.name}**${storyNote}`);
    }
    ln();
  }

  // Tech Stack
  if (techStack.length > 0) {
    ln('## Tech Stack');
    ln();
    ln('| Layer | Technology | Purpose |');
    ln('|-------|-----------|---------|');
    for (const entry of techStack) {
      ln(`| ${entry.layer} | ${entry.technology} | ${entry.purpose} |`);
    }
    ln();
  }

  // Getting Started
  ln('## Getting Started');
  ln();

  if (prereqs.length > 0) {
    ln('### Prerequisites');
    ln();
    for (const p of prereqs) {
      ln(`- ${p}`);
    }
    ln();
  }

  ln('### Installation');
  ln();
  if (setupInstructions) {
    ln(setupInstructions);
  } else {
    ln('```bash');
    ln('# Infrastructure setup (generates Docker Compose from architecture)');
    ln('# Run after planning is complete:');
    ln('# /cobolt-infra --auto');
    ln('');
    ln('# Then start services:');
    ln('docker compose up -d');
    ln('```');
    ln();
    ln('> Setup instructions will be refined after infrastructure provisioning.');
  }
  ln();

  // Roadmap
  if (milestones.length > 0) {
    ln('## Roadmap');
    ln();
    ln('| Milestone | Scope |');
    ln('|-----------|-------|');
    for (const ms of milestones) {
      ln(`| ${ms.id} | ${ms.name} |`);
    }
    ln();
  }

  // Project Structure placeholder
  ln('## Project Structure');
  ln();
  ln('See `_cobolt-output/latest/planning/architecture.md` for the full system architecture.');
  ln();

  // License
  ln('## License');
  ln();
  ln('See LICENSE file for details.');
  ln();

  return lines.join('\n');
}

// ── Commands ───────────────────────────────────────────────

function cmdGenerate(args) {
  const pd = getPlanningDir(PROJECT_ROOT, { strict: true, fallbackToLatest: true });
  if (!pd) {
    console.error('[cobolt-readme-gen] No planning directory found.');
    console.error('  Run /cobolt-plan first to generate planning artifacts.');
    process.exit(2);
  }

  const latestRoot = getLatestRoot(PROJECT_ROOT);

  // Check if user-authored README exists (no watermark = don't overwrite)
  const rootReadme = path.join(PROJECT_ROOT, 'README.md');
  if (fs.existsSync(rootReadme)) {
    const existing = fs.readFileSync(rootReadme, 'utf8');
    if (!existing.includes('Generated by CoBolt')) {
      console.warn('[cobolt-readme-gen] WARNING: Existing README.md does not have CoBolt watermark.');
      console.warn('  Skipping to preserve user-authored README.');
      console.warn('  Delete README.md or add the watermark comment to allow generation.');
      return;
    }
  }

  const readme = generateReadme(pd, latestRoot);

  // Determine output paths
  const customOutput = extractFlag(args, '--output');
  const outputs = [];

  if (customOutput) {
    outputs.push(customOutput);
  } else {
    outputs.push(rootReadme);
    const planningReadme = path.join(pd, 'project-readme.md');
    outputs.push(planningReadme);
  }

  for (const outPath of outputs) {
    const dir = path.dirname(outPath);
    if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
    fs.writeFileSync(outPath, readme, 'utf8');
    console.log(`[cobolt-readme-gen] Written: ${path.relative(PROJECT_ROOT, outPath)}`);
  }

  // Verify minimum size
  const size = Buffer.byteLength(readme, 'utf8');
  if (size < 300) {
    console.warn(`[cobolt-readme-gen] WARNING: Generated README is only ${size} bytes.`);
    console.warn('  Planning artifacts may be incomplete.');
  } else {
    console.log(`[cobolt-readme-gen] README generated (${size} bytes).`);
  }
}

function cmdCheck() {
  const rootReadme = path.join(PROJECT_ROOT, 'README.md');
  const qualityCheck = checkReadme(PROJECT_ROOT, { freshness: true });
  if (!qualityCheck.passed) {
    for (const issue of qualityCheck.issues) {
      console.error(`[cobolt-readme-gen] ${issue}`);
    }
    console.error('  Run: node tools/cobolt-readme-gen.js generate');
    process.exit(1);
  }

  if (!fs.existsSync(rootReadme)) {
    console.error('[cobolt-readme-gen] README.md not found at project root.');
    console.error('  Run: node tools/cobolt-readme-gen.js generate');
    process.exit(1);
  }

  const content = fs.readFileSync(rootReadme, 'utf8');
  if (!content.includes('Generated by CoBolt')) {
    console.log('[cobolt-readme-gen] README.md exists but is user-authored (no CoBolt watermark).');
    process.exit(0); // User-authored is fine
  }

  // Check freshness: compare README mtime against key planning artifacts
  const readmeStat = fs.statSync(rootReadme);
  const pd = getPlanningDir(PROJECT_ROOT, { strict: false, fallbackToLatest: true });

  if (!pd) {
    console.warn('[cobolt-readme-gen] No planning directory found. Cannot check freshness.');
    process.exit(0);
  }

  const keyArtifacts = ['prd.md', 'epics.md', 'architecture.md', 'milestones.md'];
  let newerArtifact = null;

  for (const artifact of keyArtifacts) {
    const artifactPath = path.join(pd, artifact);
    if (!fs.existsSync(artifactPath)) continue;
    const artifactStat = fs.statSync(artifactPath);
    if (artifactStat.mtimeMs > readmeStat.mtimeMs) {
      newerArtifact = artifact;
      break;
    }
  }

  if (newerArtifact) {
    console.error(`[cobolt-readme-gen] README.md is stale — ${newerArtifact} is newer.`);
    console.error('  Run: node tools/cobolt-readme-gen.js generate');
    process.exit(1);
  }

  console.log('[cobolt-readme-gen] README.md is up to date.');
}

// ── Helpers ────────────────────────────────────────────────

function extractFlag(args, flag) {
  const idx = args.indexOf(flag);
  if (idx === -1 || !args[idx + 1]) return null;
  return args[idx + 1];
}

function printUsage() {
  console.log('CoBolt README Gen - Deterministic README.md generator from planning artifacts');
  console.log('');
  console.log('Usage:');
  console.log('  node tools/cobolt-readme-gen.js generate [--output <path>]');
  console.log('  node tools/cobolt-readme-gen.js check');
  console.log('');
  console.log('Commands:');
  console.log('  generate   Read planning artifacts and write README.md');
  console.log('  check      Verify README.md exists and is fresh (exit 1 if stale)');
  console.log('');
  console.log('The generator preserves user-authored READMEs (without CoBolt watermark).');
}

function hasHeading(content, pattern) {
  return new RegExp(`^#{1,3}\\s+.*(?:${pattern}).*$`, 'im').test(content);
}

function checkReadme(projectRoot = PROJECT_ROOT, options = {}) {
  const rootReadme = path.join(projectRoot, 'README.md');
  const issues = [];

  if (!fs.existsSync(rootReadme)) {
    return {
      passed: false,
      issues: ['README.md not found at project root.'],
      path: rootReadme,
      generated: false,
    };
  }

  const content = fs.readFileSync(rootReadme, 'utf8');
  const generated = content.includes('Generated by CoBolt');
  const size = Buffer.byteLength(content, 'utf8');

  if (size < 300) {
    issues.push(`README.md is too small to be useful (${size}B < 300B).`);
  }

  if (!hasHeading(content, 'overview|about|summary|introduction|what is')) {
    issues.push('README.md is missing an overview/about section.');
  }

  if (!hasHeading(content, 'getting started|setup|installation|install|running locally|development')) {
    issues.push('README.md is missing setup or installation instructions.');
  }

  if (!hasHeading(content, 'usage|api|architecture|tech stack|features|project structure|roadmap')) {
    issues.push('README.md is missing usage, features, tech stack, architecture, or project structure details.');
  }

  if (options.freshness && generated) {
    const readmeStat = fs.statSync(rootReadme);
    const pd = getPlanningDir(projectRoot, { strict: false, fallbackToLatest: true });
    const keyArtifacts = ['prd.md', 'epics.md', 'architecture.md', 'milestones.md'];
    const newerArtifact = pd
      ? keyArtifacts.find((artifact) => {
          const artifactPath = path.join(pd, artifact);
          return fs.existsSync(artifactPath) && fs.statSync(artifactPath).mtimeMs > readmeStat.mtimeMs;
        })
      : null;

    if (newerArtifact) {
      issues.push(`README.md is stale; ${newerArtifact} is newer.`);
    }
  }

  return {
    passed: issues.length === 0,
    issues,
    path: rootReadme,
    generated,
    size,
  };
}

// ── Entry point ────────────────────────────────────────────

if (require.main === module) {
  const [, , command, ...args] = process.argv;

  switch (command) {
    case 'generate':
      cmdGenerate(args);
      break;
    case 'check':
      cmdCheck();
      break;
    default:
      printUsage();
      process.exit(command ? 2 : 0);
  }
}

module.exports = { generateReadme, checkReadme, WATERMARK };
